package practice_while;

public class practice {

	public static void main(String[] args) {
		int i = 1;
	    while (i <= 100) {
	        if (i % 2 == 0) {
	            System.out.println(i + "は偶数です");
	        } else {
	            System.out.println(i + "は奇数です");
	        }
	        i++;
	    }

	}

}
